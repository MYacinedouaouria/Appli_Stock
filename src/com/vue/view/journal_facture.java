    private void rechercherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rechercherActionPerformed
        // TODO add your handling code here:
        //la recherche peut renvoyer un RuntimeException ie pas de produit trouver
        try{
            //on recupere la valeur issue de la categorie � rechercher

            String valeur=val_recherche.getText();

            //on recherche en fonction de sa categorie selectionn�e dans la liste
            if(type_recherche.getSelectedItem()=="date_facture"){
                
                String val=dateFormat.format(date.getDate());
                model.l_e=r.list_facture("date", val);
            }
            else if(type_recherche.getSelectedItem()=="numero facture"){
                
                int fac=Integer.parseInt(val_recherche.getText());
                model.l_e.clear();
                model.l_e=r.list_facture(fac);
            }
            else{
                 val_recherche.setEditable(true);
                date.setEnabled(false);
                model.l_e=r.list_facture("nom_client", valeur);
            
            }

            table_fac.setModel(model);
            actualise_panel();
        }
        catch(RuntimeException r){
            jop.showMessageDialog(null,"pas de commande correspondante à cette recherche","pas de commande trouvéz",JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_rechercherActionPerformed

