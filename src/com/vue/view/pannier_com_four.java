    private void ajouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ajouterActionPerformed
        // TODO add your handling code here:
        
        Fournisseur f=new Fournisseur();
        Commande_four cf=new Commande_four();
      try{
        int selection=table_fournisseur.getSelectedRow();
        f=model.l_e.get(selection);
        Date curdate=new Date();
            java.sql.Date datesql=new java.sql.Date(curdate.getYear(),curdate.getMonth(), curdate.getDate());
        cf.setDate(datesql);
        cf.setId_four(f.getId_four());
        cf.setNom_four(f.getNom_four());
        cf.setAdresse(f.getAddresse());
        cf.setEntreprises(f.getEntreprise());
        cf.setTel_four(f.getTel_four());
        cf.setVille(f.getVille_four());
        cf.setNom_produit(nom_produit.getText());
        cf.setPrix_achat(Float.parseFloat(prix_achat.getText()));
        cf.setQte_com(Integer.parseInt(quantite.getText()));
        
        if(r.produit_existe(cf.getNom_produit())){
                int confirmation=jop.showConfirmDialog(null, "voulez vous vraiment ajouter la commande: " ,"confirmation de suppression",JOptionPane.YES_NO_CANCEL_OPTION);
 		  if(confirmation==0){
                    r.ajouter_com_four(cf);
       
                
                
       jop.showMessageDialog(null, "ajout reussi","good",JOptionPane.INFORMATION_MESSAGE);
                processus.dispose();
                journal_com_four1.actualiser.doClick();
                  }
        }
        else{
    		jop.showMessageDialog(null, "aucun produit correspondant en stock","pas de produit",JOptionPane.WARNING_MESSAGE);
    	}
    }
    catch(NumberFormatException n){
    	jop.showMessageDialog(null, "veillez remplir tout vos champs","erreur de remplissage",JOptionPane.WARNING_MESSAGE);
    }
    catch(IndexOutOfBoundsException i){
    	jop.showMessageDialog(null, "aucune selection de fournisseur","selection requise",JOptionPane.WARNING_MESSAGE);
    }
    

    }//GEN-LAST:event_ajouterActionPerformed

