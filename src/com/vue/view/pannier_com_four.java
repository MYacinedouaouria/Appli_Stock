    private void rechercherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rechercherActionPerformed
        // TODO add your handling code here:
        //la recherche peut renvoyer un RuntimeException ie pas de produit trouver
        try{
            //on recupere la valeur issue de la categorie � rechercher

            String ville=valeur_recher.getText();

            //on recherche en fonction de sa categorie selectionn�e dans la liste
            if(type_recherche.getSelectedItem()=="ville fournisseur"){
                model.l_e=r.rechercher_four_chaine(ville,"ville");
            }
            else if(type_recherche.getSelectedItem()=="code fournisseur"){
                int valeur=Integer.parseInt(valeur_recher.getText());
                model.l_e.clear();
                model.l_e.add(r.rechercher_four_code(valeur));
            }
            else if(type_recherche.getSelectedItem()=="nom fournisseur"){
                model.l_e=r.rechercher_four_chaine(ville,"nom");
            }

            table_fournisseur.setModel(model);
            actualise_panel();
        }
        catch(NumberFormatException n){
            jop.showMessageDialog(null, "veillez saisir dans le champ de recherche","erreur de remplissage",JOptionPane.WARNING_MESSAGE);
        }
        catch(RuntimeException r){
            jop.showMessageDialog(null,"pas de fournisseur correspondant � cette recherche","pas de fournisseur trouver",JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_rechercherActionPerformed

